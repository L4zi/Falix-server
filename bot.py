import discord
from discord.ext import commands
import os
import asyncio
import aiohttp
from urllib.parse import quote

DISCORD_TOKEN = os.getenv("DISCORD_TOKEN")

intents = discord.Intents.default()
bot = commands.Bot(command_prefix="!", intents=intents)

@bot.tree.command(name="imagine", description="Generate an AI image from a prompt")
async def imagine(interaction: discord.Interaction, prompt: str):
    await interaction.response.defer()

    encoded = quote(prompt)
    url = f"https://image.pollinations.ai/prompt/{encoded}?nologo=true&width=1024&height=1024"

    async with aiohttp.ClientSession() as session:
        async with session.get(url) as r:
            if r.status == 200:
                image_data = await r.read()
                file = discord.File(
                    fp=__import__("io").BytesIO(image_data),
                    filename="image.png"
                )
                embed = discord.Embed(
                    description=f"**{prompt}**",
                    color=discord.Color.purple()
                )
                embed.set_image(url="attachment://image.png")
                embed.set_footer(text="Generated by falixbot")
                await interaction.followup.send(embed=embed, file=file)
            else:
                await interaction.followup.send("Failed to generate image. Try again.", ephemeral=True)

@bot.event
async def on_ready():
    await bot.tree.sync()
    print(f"Logged in as {bot.user}")

bot.run(DISCORD_TOKEN)